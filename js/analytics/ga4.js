/**
 * Google Analytics 4 Integration
 * UGC Maroc - Analytics avec consentement RGPD
 */
class GA4Analytics {
  constructor() {
    this.measurementId = 'GA_MEASUREMENT_ID'; // √Ä remplacer par votre ID
    this.isLoaded = false;
    this.isInitialized = false;
    this.dataLayer = window.dataLayer || [];
  }

  /**
   * Initialiser Google Analytics 4
   * @param {Object} userData - Donn√©es utilisateur
   */
  init(userData = {}) {
    if (this.isInitialized) return;

    // V√©rifier le consentement analytics
    if (!window.cookieManager || !window.cookieManager.hasConsent('analytics')) {
      console.log('üîí GA4 blocked: No analytics consent');
      return;
    }

    this.loadScript(() => {
      this.setupGA4(userData);
      this.isInitialized = true;
      console.log('‚úÖ Google Analytics 4 initialized');
    });
  }

  /**
   * Charger le script GA4
   * @param {Function} callback - Callback apr√®s chargement
   */
  loadScript(callback) {
    if (this.isLoaded) {
      callback();
      return;
    }

    // Initialiser dataLayer
    window.dataLayer = window.dataLayer || [];
    function gtag(){dataLayer.push(arguments);}
    window.gtag = gtag;
    gtag('js', new Date());

    // Charger le script GA4
    const script = document.createElement('script');
    script.async = true;
    script.src = `https://www.googletagmanager.com/gtag/js?id=${this.measurementId}`;
    script.onload = callback;
    document.head.appendChild(script);
    
    this.isLoaded = true;
  }

  /**
   * Configurer GA4 avec les donn√©es utilisateur
   * @param {Object} userData - Donn√©es utilisateur
   */
  setupGA4(userData) {
    if (!window.gtag) return;

    // Configuration de base
    window.gtag('config', this.measurementId, {
      anonymize_ip: true,
      cookie_flags: 'SameSite=None;Secure',
      send_page_view: false, // Nous g√©rons manuellement
      custom_map: {
        'custom_parameter_1': 'user_role',
        'custom_parameter_2': 'user_language',
        'custom_parameter_3': 'platform_version'
      }
    });

    // User properties
    if (userData.userId) {
      window.gtag('set', 'user_id', userData.userId);
    }
    
    if (userData.role) {
      window.gtag('set', 'user_role', userData.role);
    }
    
    if (userData.language) {
      window.gtag('set', 'user_language', userData.language);
    }

    // Propri√©t√©s personnalis√©es UGC Maroc
    window.gtag('set', 'platform', 'ugc-maroc');
    window.gtag('set', 'platform_version', '1.0.0');
    window.gtag('set', 'user_type', userData.role || 'anonymous');

    // Envoyer page view initial
    this.trackPageView();
  }

  /**
   * Tracker une page view
   * @param {string} pagePath - Chemin de la page
   * @param {string} pageTitle - Titre de la page
   */
  trackPageView(pagePath = null, pageTitle = null) {
    if (!this.isInitialized || !window.gtag) return;

    window.gtag('event', 'page_view', {
      page_path: pagePath || window.location.pathname,
      page_title: pageTitle || document.title,
      page_location: window.location.href
    });

    console.log('üìÑ GA4 page view tracked:', pagePath || window.location.pathname);
  }

  /**
   * Tracker un √©v√©nement personnalis√©
   * @param {string} eventName - Nom de l'√©v√©nement
   * @param {Object} parameters - Param√®tres de l'√©v√©nement
   */
  trackEvent(eventName, parameters = {}) {
    if (!this.isInitialized || !window.gtag) return;

    // √âv√©nements m√©tier UGC Maroc
    const eventConfig = this.getEventConfig(eventName, parameters);
    
    window.gtag('event', eventName, eventConfig);
    
    console.log(`üìä GA4 event tracked: ${eventName}`, eventConfig);
  }

  /**
   * Obtenir la configuration d'un √©v√©nement
   * @param {string} eventName - Nom de l'√©v√©nement
   * @param {Object} parameters - Param√®tres
   * @returns {Object} Configuration de l'√©v√©nement
   */
  getEventConfig(eventName, parameters) {
    const baseConfig = {
      event_category: 'UGC Maroc',
      platform: 'ugc-maroc',
      ...parameters
    };

    // Configurations sp√©cifiques par √©v√©nement
    const eventConfigs = {
      // Campagnes
      'campaign_viewed': {
        event_category: 'Campaign',
        event_label: parameters.campaignId,
        value: parameters.budget
      },
      'campaign_created': {
        event_category: 'Campaign',
        event_label: 'Campaign Created',
        value: parameters.budget
      },
      'campaign_applied': {
        event_category: 'Campaign',
        event_label: parameters.campaignId,
        value: parameters.budget
      },
      
      // Gigs
      'gig_viewed': {
        event_category: 'Gig',
        event_label: parameters.gigId,
        value: parameters.price
      },
      'gig_created': {
        event_category: 'Gig',
        event_label: 'Gig Created',
        value: parameters.price
      },
      'gig_ordered': {
        event_category: 'Gig',
        event_label: parameters.gigId,
        value: parameters.price
      },
      
      // N√©gociations
      'negotiation_started': {
        event_category: 'Negotiation',
        event_label: parameters.negotiationId
      },
      'negotiation_message_sent': {
        event_category: 'Negotiation',
        event_label: parameters.negotiationId
      },
      
      // Vid√©os
      'video_uploaded': {
        event_category: 'Video',
        event_label: parameters.videoId,
        value: parameters.fileSize
      },
      'video_viewed': {
        event_category: 'Video',
        event_label: parameters.videoId
      },
      'video_approved': {
        event_category: 'Video',
        event_label: parameters.videoId,
        value: parameters.amount
      },
      
      // Paiements
      'payment_initiated': {
        event_category: 'Payment',
        event_label: parameters.paymentId,
        value: parameters.amount,
        currency: 'MAD'
      },
      'payment_completed': {
        event_category: 'Payment',
        event_label: parameters.paymentId,
        value: parameters.amount,
        currency: 'MAD'
      },
      'withdrawal_requested': {
        event_category: 'Withdrawal',
        event_label: parameters.withdrawalId,
        value: parameters.amount,
        currency: 'MAD'
      },
      
      // Navigation
      'search_performed': {
        event_category: 'Search',
        event_label: parameters.query,
        search_term: parameters.query
      },
      'filter_applied': {
        event_category: 'Filter',
        event_label: parameters.filterType
      },
      
      // Erreurs
      'error_occurred': {
        event_category: 'Error',
        event_label: parameters.errorType,
        error_message: parameters.message
      }
    };

    return { ...baseConfig, ...eventConfigs[eventName] };
  }

  /**
   * Tracker une conversion e-commerce
   * @param {Object} transactionData - Donn√©es de transaction
   */
  trackPurchase(transactionData) {
    if (!this.isInitialized || !window.gtag) return;

    window.gtag('event', 'purchase', {
      transaction_id: transactionData.transactionId,
      value: transactionData.value,
      currency: transactionData.currency || 'MAD',
      items: transactionData.items || []
    });

    console.log('üí∞ GA4 purchase tracked:', transactionData);
  }

  /**
   * Tracker un item ajout√© au panier
   * @param {Object} itemData - Donn√©es de l'item
   */
  trackAddToCart(itemData) {
    if (!this.isInitialized || !window.gtag) return;

    window.gtag('event', 'add_to_cart', {
      currency: 'MAD',
      value: itemData.price,
      items: [{
        item_id: itemData.itemId,
        item_name: itemData.name,
        item_category: itemData.category,
        price: itemData.price,
        quantity: 1
      }]
    });

    console.log('üõí GA4 add to cart tracked:', itemData);
  }

  /**
   * Identifier un utilisateur
   * @param {string} userId - ID utilisateur
   * @param {Object} userProperties - Propri√©t√©s utilisateur
   */
  identify(userId, userProperties = {}) {
    if (!this.isInitialized || !window.gtag) return;

    window.gtag('set', 'user_id', userId);
    
    // Propri√©t√©s utilisateur
    Object.entries(userProperties).forEach(([key, value]) => {
      window.gtag('set', key, value);
    });

    console.log(`üë§ GA4 user identified: ${userId}`, userProperties);
  }

  /**
   * D√©finir une propri√©t√© personnalis√©e
   * @param {string} key - Cl√© de la propri√©t√©
   * @param {*} value - Valeur de la propri√©t√©
   */
  setProperty(key, value) {
    if (!this.isInitialized || !window.gtag) return;
    
    window.gtag('set', key, value);
  }

  /**
   * Tracker une erreur
   * @param {string} errorType - Type d'erreur
   * @param {Object} errorData - Donn√©es de l'erreur
   */
  trackError(errorType, errorData = {}) {
    if (!this.isInitialized || !window.gtag) return;

    window.gtag('event', 'exception', {
      description: errorData.message || errorType,
      fatal: errorData.fatal || false,
      error_type: errorType,
      error_code: errorData.code
    });

    console.log(`‚ùå GA4 error tracked: ${errorType}`, errorData);
  }

  /**
   * Tracker un timing
   * @param {string} name - Nom du timing
   * @param {number} value - Valeur en millisecondes
   * @param {string} category - Cat√©gorie du timing
   */
  trackTiming(name, value, category = 'Performance') {
    if (!this.isInitialized || !window.gtag) return;

    window.gtag('event', 'timing_complete', {
      name: name,
      value: value,
      event_category: category
    });

    console.log(`‚è±Ô∏è GA4 timing tracked: ${name} = ${value}ms`);
  }

  /**
   * V√©rifier si GA4 est disponible
   * @returns {boolean} True si disponible
   */
  isAvailable() {
    return this.isInitialized && window.gtag;
  }

  /**
   * Obtenir les donn√©es de session
   * @returns {Object} Donn√©es de session GA4
   */
  getSessionData() {
    if (!this.isAvailable()) return null;
    
    return {
      measurementId: this.measurementId,
      isLoaded: this.isLoaded,
      isInitialized: this.isInitialized,
      dataLayer: this.dataLayer
    };
  }
}

// Initialiser GA4 Analytics
window.ga4Analytics = new GA4Analytics();

// Auto-initialiser si consentement d√©j√† donn√©
document.addEventListener('DOMContentLoaded', () => {
  if (window.cookieManager && window.cookieManager.hasConsent('analytics')) {
    const userData = {
      userId: window.cookieManager.getCurrentUserId(),
      role: document.body.dataset.userRole,
      language: window.cookieManager.getPreferences().language
    };
    
    window.ga4Analytics.init(userData);
  }
});

// √âcouter les changements de consentement
window.addEventListener('consentChanged', (event) => {
  const { consent } = event.detail;
  
  if (consent.analytics && !window.ga4Analytics.isInitialized) {
    const userData = {
      userId: window.cookieManager.getCurrentUserId(),
      role: document.body.dataset.userRole,
      language: window.cookieManager.getPreferences().language
    };
    
    window.ga4Analytics.init(userData);
  }
});

// Exporter pour utilisation dans d'autres modules
if (typeof module !== 'undefined' && module.exports) {
  module.exports = GA4Analytics;
}
